package compositionEX;

public class Dimensions {

    private int heigth;
    private int width;
    private int depth;

    public Dimensions(int heigth, int width, int depth) {
        this.heigth = heigth;
        this.width = width;
        this.depth = depth;
    }

    public int getHeigth() {
        return heigth;
    }

    public int getWidth() {
        return width;
    }

    public int getDepth() {
        return depth;
    }
}
